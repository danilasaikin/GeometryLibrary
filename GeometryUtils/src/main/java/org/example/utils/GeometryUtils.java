package org.example.utils;

public class GeometryUtils {
    // Функция для перевода радиуса круга в диаметр
    public static double radiusToDiameter(double radius) {
        return 2 * radius;
    }

    // Функция для перевода диаметра круга в радиус
    public static double diameterToRadius(double diameter) {
        return diameter / 2;
    }

    // Дополнительные функции могут быть добавлены по мере необходимости
}
